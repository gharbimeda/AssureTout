package tn.esprit.assuretout.entities;

public class Image {
	
	String name;
	String description;
	
	
	
	
	
	public Image() {
		super();
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
	

}
